bool process_record_user(uint16_t keycode, keyrecord_t *record)
{
  switch (keycode)
  {
  case LOWER:
    if (record->event.pressed)
    {
      layer_on(_LOWER);
    }
    else
    {
      layer_off(_LOWER);
    }
    return false;
    break;
  case RAISE:
    if (record->event.pressed)
    {
      layer_on(_RAISE);
    }
    else
    {
      layer_off(_RAISE);
    }
    return false;
    break;
  }
  return true;
}